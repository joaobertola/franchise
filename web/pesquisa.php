<p align="center"><strong style="color:#FF6600">Legisla&ccedil;&atilde;o Aplicada e Acesso pleno &agrave;s Informa&ccedil;&otilde;es</strong></p>
<p>Os Servi&ccedil;os de Prote&ccedil;&atilde;o ao Cr&eacute;dito s&atilde;o considerados entes de carater p&uacute;blico, ainda que mantidos pela iniciativa privada e s&atilde;o devidamente regulamentados pelo C&oacute;digo de Defesa do Consumidor Sess&atilde;o VI - Dos Bancos de Dados e Cadastros de Consumidores, art.43 a 45 do cap&iacute;tulo V - Das Pr&aacute;ticas Comerciais de tais Informa&ccedil;&otilde;es.</p>
<p>Dos Bancos de Dados s&atilde;o exigidos:</p>
<ol>
  <li>Objetividade e Veracidade nas informa&ccedil;&otilde;es.</li>
  <li>Devem ser escritas em linguagem de f&aacute;cil compreens&atilde;o.</li>
  <li>N&atilde;o podendo haver informa&ccedil;&otilde;es negativas referidas a per&iacute;odos superiores a 5 (cinco) anos.</li>
  <li>Comunica&ccedil;&atilde;o por escrito ao Consumidor, quando a abertura de cadastro ou banco de dados caso n&atilde;o for solicitada por ele.</li>
  <li>Corre&ccedil;&atilde;o imediata e comunica&ccedil;&atilde;o, no prazo de 5 (cinco) dias &uacute;teis aos eventuais destinat&aacute;rios das informa&ccedil;&otilde;es, de quaisquer inexatid&otilde;es em dados ou cadastros, se o consumidor requerer.</li>
</ol>
<p>Os arquivos de Banco de Dados de restritivos tem como objetivo o armazenamento de informa&ccedil;&atilde;o destinada a prote&ccedil;&atilde;o de empres&aacute;rios e  dar cr&eacute;dito &agrave; pr&oacute;pria sociedade. </p>
    <p>Estabelece o artigo 220 da Constitui&ccedil;&atilde;o Federal, que: &quot;A manifesta&ccedil;&atilde;o do pensamento, a cria&ccedil;&atilde;o, a express&atilde;o e a informa&ccedil;&atilde;o, sob qualquer forma, processo ou ve&iacute;culo n&atilde;o sofrer&atilde;o qualquer restri&ccedil;&atilde;o ...&quot;.</p>
    <p>Em conson&acirc;ncia com o texto constitucional  assegura como garant&iacute;a fundamental o acesso &agrave; informa&ccedil;&atilde;o, nos termos do art. 5&ordm;, inciso XIV, do referido diploma legal.</p>
